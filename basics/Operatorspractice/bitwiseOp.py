# shift operators
a = 10
b = -10

# print bitwise right shift operator
print("a >> 1 =", a >> 1)
print("b >> 1 =", b >> 1)

# print bitwise left shift operator
print("a << 1 =", a << 1)
print("b << 1 =", b << 1)